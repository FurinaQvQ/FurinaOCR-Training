class GenshinArtifactTextGenerator(TextGenerator):
    """
    原神圣遗物名称文本生成器
    
    从预定义的圣遗物名称列表中随机选择一个名称
    支持所有游戏内圣遗物套装的名称生成
    
    用途：训练OCR模型识别圣遗物名称文本
    """
    
    def __init__(self):
        super(GenshinArtifactTextGenerator, self).__init__("Genshin Artifact Text Generator")

    def generate_text(self):
        """
        生成圣遗物名称文本
        
        Returns:
            str: 随机选择的圣遗物名称
        """
        return random.choice(artifact_name)

    def get_lexicon(self):
        """
        获取生成文本所需的词汇表
        
        Returns:
            set: 包含所有圣遗物名称中使用的字符集合
        """
        ret = set()
        for name in artifact_name:
            for char in name:
                ret.add(char)
        return ret
